def calculate_volume_of_sphere(radius):
    vol = (4/3)*3.14*(radius**3)
    return vol

r1 = 30
vol1 = calculate_volume_of_sphere(r1)
print(f"The volumne of sphere with radius {r1} is: {vol1}")

r2 = 40
vol2 = calculate_volume_of_sphere(r2)
print(f"The volumne of sphere with radius {r2} is: {vol2}")


